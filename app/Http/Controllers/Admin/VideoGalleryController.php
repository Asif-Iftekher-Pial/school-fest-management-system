    }

    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        $data = Videogallery::findOrFail($id);
        $data->delete();
        return redirect('admin/videogallery')->with('flash_message', 'Video deleted!');
    }
